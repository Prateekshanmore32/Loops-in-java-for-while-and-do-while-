package loops;

import java.util.Scanner;

public class DoWhileLoop {

	public static void main(String[] args) {

			Scanner sc=new Scanner(System.in);
			int n;
			do
			{
				n=sc.nextInt();
				System.out.println("You Entered "+ n);
			}while(n!=0);

	}

}


